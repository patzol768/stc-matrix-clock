// Determines character width
//
// Returns 0 if char not handled
//

uint8_t get_width(unsigned char c)
{
    uint8_t width;
    uint8_t* data;

    if ((c >= '0' && c <= '9') || c >= '@')
    {
        return 4;
    }

    if (c == ' ')
    {
        return 1;
    }

    width = 4;
    data = get_data_ptr(c) + width;

    // search for the last non-empty column
    //
    while (--width > 0 && *(--data) == 0x00)
        ;
    return width + 1;
}

//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
// Returns a pointer to the
//

uint8_t* get_data_ptr(unsigned char c)
{
    unsigned int begin = c;
    begin <<= 2;
    return font_data + begin;
}
